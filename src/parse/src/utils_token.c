#include "lexer.h"

int	is_operator_char(char c)
{
	if (c == '|' || c == '&' || c == '<' || c == '>' || c == '('|| c == ')'
		|| c == '$' || c == '`' || c == ' '		|| c == '\t' || c == '\n')
		return (1);
	return (0);
}
